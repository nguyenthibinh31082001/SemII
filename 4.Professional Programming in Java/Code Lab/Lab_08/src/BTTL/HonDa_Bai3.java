public class HonDa_Bai3{
    private String maXe;
    private String model;
    private int thongSoXe;

    public HonDa_Bai3(String maXe, String model, int thongSoXe) {
        this.maXe = maXe;
        this.model = model;
        this.thongSoXe = thongSoXe;
    }

    public String getMaXe() {
        return maXe;
    }

    public void setMaXe(String maXe) {
        this.maXe = maXe;
    }

    public String getModel() {
        return model;
    }

    public void setModel(String model) {
        this.model = model;
    }

    public int getThongSoXe() {
        return thongSoXe;
    }

    public void setThongSoXe(int thongSoXe) {
        this.thongSoXe = thongSoXe;
    }

    
    
}
